    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String title = titleTxt.getText();
        if(title.isEmpty()){
            
            
        JOptionPane.showMessageDialog(CRUDNetflixPanel.this,""+"Please provide a valid title.","Adding Shows",JOptionPane.ERROR_MESSAGE);
        return;}
        
        String releaseDate = releaseDateTxt.getText();
        //Exception for release date
        
        Quality quality = (Quality)qualityCombo.getSelectedItem();
        Genres genre = (Genres)genreCombo.getSelectedItem();
        Language language = (Language)langCombo.getSelectedItem();
        String names = actorsTxt.getText();
        String numOfViewsStr = numOfViewsTxt.getText();
        int numOfViews = 0;
        
        try{
            numOfViews = Integer.parseInt(numOfViewsStr);
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(CRUDNetflixPanel.this,"Please provide a valid number.","Adding Shows",JOptionPane.ERROR_MESSAGE);
        }
        
        String averageRatingStr = averageRatingTxt.getText();
        double averageRating = 0.0;
        try{
        averageRating = Double.parseDouble(averageRatingStr);
        }catch(NumberFormatException ex1){
            JOptionPane.showMessageDialog(CRUDNetflixPanel.this,"Please provide a valid number.","Adding Shows",JOptionPane.ERROR_MESSAGE);
        }
        String synopsis = synopsisTxt.getText();
        Show SH = new Show(title,releaseDate,quality,genre,language,names,numOfViews,averageRating,synopsis);
        //
        ShowCollection.addShow(SH);
        model.addElement(SH);
        
        
        titleTxt.setText("");
        releaseDateTxt.setText("");
        actorsTxt.setText("");
        numOfViewsTxt.setText("");
        averageRatingTxt.setText("");
        synopsisTxt.setText("");

        showsList.setSelectedValue(null, true);
    }//GEN-LAST:event_addBtnActionPerformed

