    private void showsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_showsListValueChanged
        if(evt.getValueIsAdjusting()) return;
        Show SH = showsList.getSelectedValue();
        
        if(SH == null) return;
        
        titleTxt.setText(SH.getTitle());
        releaseDateTxt.setText(SH.getReleaseDate());
        qualityCombo.setSelectedItem(SH.getQuality().name());
        genreCombo.setSelectedItem(SH.getGenres().name());
        langCombo.setSelectedItem(SH.getLanguage().name());
        actorsTxt.setText(SH.getNames());
        numOfViewsTxt.setText(String.valueOf(SH.getNumOfViews()));
        averageRatingTxt.setText(String.valueOf(SH.getAverageRating()));
        synopsisTxt.setText(SH.getSynopsis());

    }//GEN-LAST:event_showsListValueChanged

